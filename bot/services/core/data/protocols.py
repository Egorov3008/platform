from typing import Protocol, List, TypeVar, Optional, Dict, Any, Generic

import asyncpg

from services.cache.service import T

T = TypeVar("T")


class DataProtocol(Generic[T], Protocol):
    async def get_data(self, identifier: int | str): ...
    async def get_all(self) -> List[T]: ...
    async def exists(self, identifier: int) -> bool: ...
    async def count(self) -> int: ...
    async def save_data(self, conn: asyncpg.Pool, data: T, **kwargs) -> None: ...
    async def delete_data(self, conn: asyncpg.Pool, data: T) -> None: ...
    async def get_by(self, **kwargs) -> List[Optional[T]]: ...
    async def update(
        self, conn: asyncpg.Pool, data: T, search_data: Dict[str, Any]
    ) -> T: ...
