-- ============================================================================
-- Миграция 013: Удаление таблицы inbound
-- ============================================================================
-- Описание: Удаляет таблицу inbound — сущность Inbound больше не используется.
-- Доступные подключения теперь берутся из панели 3x-UI, отфильтрованные по
-- AVAILABLE_CONNECTIONS из .env (см. FormConnectionData).
--
-- Колонка keys.inbound_id СОХРАНЯЕТСЯ — в ней хранится panel inbound_id из 3x-UI
-- (например 11, 12), а не SERIAL PK таблицы inbound. FK keys.inbound_id -> inbound(id)
-- уже удалён в schema.sql, поэтому таблица inbound больше не используется.
--
-- Идемпотентно: ничего не делает, если таблицы нет.
-- Приоритет: Низкий (чистка устаревшей таблицы)
-- ============================================================================

BEGIN;

DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.tables WHERE table_name = 'inbound'
    ) THEN
        DROP TABLE inbound;
        RAISE NOTICE 'Миграция 013: таблица inbound удалена';
    ELSE
        RAISE NOTICE 'Миграция 013: таблица inbound отсутствует, удалять нечего';
    END IF;
END $$;

COMMIT;